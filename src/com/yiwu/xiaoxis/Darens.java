package com.yiwu.xiaoxis;

public class Darens {
	private String name;
	private String intro;
	public Darens(String name,String intro)
	{
		setName(name);
		setIntro(intro);
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getIntro() {
		return intro;
	}
	public void setIntro(String intro) {
		this.intro = intro;
	}
	
	

}
